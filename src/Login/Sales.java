    public  void upDateDB()
    {
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            sqlConn = DriverManager.getConnection(dataConn,username,password);
            pst = sqlConn.prepareStatement("select * from sales");
            
            rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            
            q = stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next()){
                    Vector columnData = new Vector();
                    
                    for(i=1;i<=q; i++){
                        
                        columnData.add(rs.getString("SalesID"));
                        columnData.add(rs.getString("CustomerID"));
                        columnData.add(rs.getString("TotalPrice"));
                        columnData.add(rs.getString("Discount"));
                        columnData.add(rs.getString("NetTotal"));
                    }
                    RecordTable.addRow(columnData);
            }
                                                    
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
